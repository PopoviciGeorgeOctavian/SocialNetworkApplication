package com.socialnetwork.lab78.utils.observer;


/**
 * The `ChangeEventType` enum represents different types of change events related to users.
 * It includes ADD, UPDATE, and REMOVE events.
 */
public enum ChangeEventType {

    // Represents adding a user
    ADD,

    // Represents updating a user
    UPDATE,

    // Represents removing a user
    REMOVE;
}